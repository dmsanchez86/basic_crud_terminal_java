    private void item_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_item_updateActionPerformed
        int row = list_books.getSelectedRow();
        
        options_book.setVisible(false);
        update_dialog.setVisible(true);
        update_dialog.setSize(95, 162);
        update_dialog.setLocation(500, 300);
        update_dialog.setResizable(false);
        
        if(row != -1){
            id_book = Integer.parseInt(list_books.getValueAt(row, 0).toString());
            name_book = list_books.getValueAt(row, 1).toString();
            txt_id_update.setText(""+ id_book);
            txt_id_update.setEnabled(false);
            txt_update_name.setText(name_book);
            
            last_name = name_book;
        }else{
            JOptionPane.showMessageDialog(this, "Select one row!", "Select Row", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_item_updateActionPerformed

    private void item_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_item_deleteActionPerformed
        int row = list_books.getSelectedRow();
        
        options_book.setVisible(false);
        
        if(row != -1){
            int i = JOptionPane.showConfirmDialog(this, "Are you sure?", "Delete Book",JOptionPane.INFORMATION_MESSAGE);
            if(i == 0){
                id_book = Integer.parseInt(list_books.getValueAt(row, 0).toString());
                if(book.delete(id_book) > 0){
                    JOptionPane.showMessageDialog(this, "the book was deleted succesful!","Delete Book",JOptionPane.INFORMATION_MESSAGE);
                    remove_items();
                    list_books();
                }else{
                    JOptionPane.showMessageDialog(this, "the book can't deleted!","Delete Book",JOptionPane.INFORMATION_MESSAGE);
                }
            }
        }else{
            JOptionPane.showMessageDialog(this, "Select one row!", "Select Row", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_item_deleteActionPerformed

    private void btn_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_updateActionPerformed
        id_book = Integer.parseInt(txt_id_update.getText());
        name_book = txt_update_name.getText();
        
        if(!last_name.equals(name_book)){
            int res = book.update(id_book, name_book);

            if(res > 0){
                JOptionPane.showMessageDialog(this, "the book was updated succesful!","Update Book",JOptionPane.INFORMATION_MESSAGE);
                update_dialog.setVisible(false);
                remove_items();
                list_books();
            }else{
                JOptionPane.showMessageDialog(this, "the book can't Updated!","Update Book",JOptionPane.INFORMATION_MESSAGE);
            }
        }else{
            update_dialog.setVisible(false);
        }
    }//GEN-LAST:event_btn_updateActionPerformed

