    private void list_books() {
        ResultSet data = book.data_books();
        Object[] obj = new Object[2];
        
        try {
            while(data.next()){
                obj[0] = data.getInt("id");
                obj[1] = data.getString("name");
                model.addRow(obj);
            }        
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }
        
        list_books.setModel(model);
    }

    private void remove_items() {
        int rows = list_books.getRowCount();
        for( int i = model.getRowCount() - 1; i >= 0; i-- )
            model.removeRow(i);
        list_books.setModel(model);
    }

    private void new_book() {
        name_book = txt_name.getText();
        
        if("".equals(txt_id.getText())){
            JOptionPane.showMessageDialog(null, "Id Can't empty!", "Add New Book", JOptionPane.INFORMATION_MESSAGE);
            txt_id.requestFocus();
        }else if("".equals(name_book)){
            JOptionPane.showMessageDialog(null, "Name Can't empty!", "Add New Book", JOptionPane.INFORMATION_MESSAGE);
            txt_name.requestFocus();
        }else{
            id_book = Integer.parseInt(txt_id.getText());
            result = book.create(id_book, name_book);

            if(result > 0){
                JOptionPane.showMessageDialog(null, "Book added succesfull", "Add New Book", JOptionPane.INFORMATION_MESSAGE);
                remove_items();
                list_books();
                txt_id.setText("");
                txt_name.setText("");
            }else{
                JOptionPane.showMessageDialog(null, "Book can't added succesfull", "Add New Book", JOptionPane.ERROR_MESSAGE);
            }
        }
    }
